#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	i;

	if (*str == '\0')
	{
		return (1);
	}
	i = 0;
	while (str[i])
	{
		if (str[i] > 90 || str[i] < 65)
		{
			return (0);
		}
		i++;
	}
	return (1);
}
